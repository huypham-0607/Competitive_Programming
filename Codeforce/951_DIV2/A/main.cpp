/*Constants*/
const int N = 5e5+10;
const int INF = 1e9+7;
const long long LLINF = 1e18+3;

/*TestCases*/
int test=1;
void solve();
void TestCases(bool v){
    if (v) cin >> test;
    while(test--) solve();
}

/*Global Variables*/
int n;

/*Solution*/
void solve(){
    cin >> n;
    int x, prev, ans = INF;
    for (int i=1; i<=n; i++){
        cin >> x;
        if (i>1) ans = min(ans,max(x,prev));
        prev = x;
    }
    cout << ans-1 << endl;
}

/*Driver Code*/
