set<int> s;

int random(int l, int r)
{
    return l + rd() % (r - l + 1);
}

int sol()
{
    int ans = 0;
    int a, b, x; cin >> a >> b >> x;
    if (a == b)
    {
        int temp = 1;
        for (int i = 0; temp <= x; ++i)
        {
            if (x % temp == 0) ans++;
            temp *= a;
        }
        return ans;
    }

    int temp1 = 1;
    for (int i = 0; temp1 <= x; ++i)
    {
        int temp2 = 1;
        for (int j = 0; temp2 * temp1 <= x; ++j)
        {
            if (x % (temp1 * temp2) == 0)
            {
                s.insert(x / (temp1 * temp2));
            }
            temp2 *= b;
        }
        temp1 *= a;
    }

    return s.size();
}

